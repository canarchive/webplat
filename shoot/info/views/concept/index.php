                    关于OPPO
                </strong>
            </p>
            <p>
                OPPO全称广东欧珀移动通信有限公司，成立于2004年。是一家全球性的智能终端制造商和移动互联网服务提供商，致力于为客户提供先进和精致的智能手机、高端影音设备和移动互联网产品与服务，业务覆盖中国、美国、俄罗斯、欧洲、东南亚等广大市场。作为一个年轻的探索者，我们从一开始就不甘心做一个平凡的品牌。怀着探索未知的强烈好奇心，OPPO先后进入MP3、MP4、蓝光高清影音、手机和移动互联网等领域。如今OPPO产品和服务已覆盖中国、美国、俄罗斯、欧洲、东南亚等广大市场，现正致力于打造专业化的智能手机与移动互联网公司。
