    var timer = 0;
    var $ = window.jQuery;
    $('.w1 a').hover(function() {
        var self = $(this),
        target = self.attr('rel');
        $('.dropMenu').hide();
        $('.w1').removeClass('cur');
        $(this).parent().addClass('cur');
        $('#' + target).stop().slideDown('slow');
        clearTimeout(timer);
    },
    function() {
        var self = $(this);
        timer = setTimeout(function() {
            var target = self.attr('rel');
            $('.dropMenu').hide();
            $('#' + target).stop().slideUp();
            self.parent().removeClass('cur');
        },
        500);
    });
    $('.dropMenu').hover(function() {
        clearTimeout(timer);
    },
    function() {
        $(this).stop().slideUp();
        $('.w1 a[rel=' + $(this).attr('id') + ']').parent().removeClass('cur');
    });
</script>
